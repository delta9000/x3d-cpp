// tools/x3d-cli/canon_gate.cpp
// ─────────────────────────────────────────────────────────────────────────────
// X3DC14N Tiered Gate — three-tier differential test for x3d canonicalize:
//
//   TIER 1  Idempotence (Java-FREE, HARD CI gate):
//     canonicalize(canonicalize(x)) == canonicalize(x) byte-for-byte over
//     the XML subset. A canonical form MUST be a fixed point. 100% required.
//     ANY idempotence failure is always a regression — no exceptions.
//
//   TIER 2  Tolerant diff vs X3DJSAIL (structural agreement, baseline-locked):
//     Compare OUR canonical output to committed X3DJSAIL golden fixtures
//     (tools/x3d-cli/goldens/canonical-goldens/) with a TOLERANT text diff:
//     numbers compared within relative tolerance, insignificant whitespace
//     normalized. Agreement = our structural + numeric form matches the oracle.
//
//   TIER 3  Byte-exact (stretch metric, informative):
//     Fraction of subset files where OUR canonical output == X3DJSAIL's byte-
//     for-byte. Reports the gap; NOT a gate.
//
// Exit modes:
//   Default (informative): Tier-1 failures → exit 1; rest → exit 0.
//   --write-baseline: writes canon-gate-baseline.tsv with per-item T1+T2 state.
//   --gate: Tier-1 ANY failure → exit 1 (hard gate, 100% required).
//           Tier-2 per-item: PASS->FAIL is a regression → exit 1.
//           FAIL->PASS (improvement) → note to refresh baseline.
//
// Usage:
//   canon_gate <x3d_binary> <corpus_root> <goldens_dir> [--gate | --write-baseline]
//
//   x3d_binary   — path to the built x3d CLI binary
//   corpus_root  — a local X3D conformance archive checkout ($X3D_CORPUS_DIR)
//   goldens_dir  — tools/x3d-cli/goldens
//
// The golden fixtures for tier 2/3 live in:
//   <goldens_dir>/canonical-goldens/<relpath>Canonical.xml
// They are generated by gen_canon_goldens.sh (run once, committed).
// If no goldens directory exists, tier 2/3 are skipped (tier 1 always runs).
// ─────────────────────────────────────────────────────────────────────────────
